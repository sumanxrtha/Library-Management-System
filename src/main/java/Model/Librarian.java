package Model;

public class Librarian {
    private int librarianId;
    private String librarianUsername;
    private String librarianName;
    private String librarianEmail;
    private int librarianMob;
    private String librarianPass;

    public Librarian() {
    }

    public int getLibrarianId() {
        return librarianId;
    }

    public void setLibrarianId(int librarianId) {
        this.librarianId = librarianId;
    }

    public String getLibrarianUsername() {
        return librarianUsername;
    }

    public void setLibrarianUsername(String librarianUsername) {
        this.librarianUsername = librarianUsername;
    }

    public String getLibrarianName() {
        return librarianName;
    }

    public void setLibrarianName(String librarianName) {
        this.librarianName = librarianName;
    }

    public String getLibrarianEmail() {
        return librarianEmail;
    }

    public void setLibrarianEmail(String librarianEmail) {
        this.librarianEmail = librarianEmail;
    }

    public int getLibrarianMob() {
        return librarianMob;
    }

    public void setLibrarianMob(int librarianMob) {
        this.librarianMob = librarianMob;
    }

    public String getLibrarianPass() {
        return librarianPass;
    }

    public void setLibrarianPass(String librarianPass) {
        this.librarianPass = librarianPass;
    }
}
